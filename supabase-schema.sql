create or replace function public.jawnix_snapshot_states()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  select coalesce(licensed_states, '{}') into new.states_snapshot
  from public.jawnix_profiles
  where user_id = new.user_id;
  new.states_snapshot := coalesce(new.states_snapshot, '{}');
  return new;
end;
$$;

drop trigger if exists jawnix_snapshot_states_before_insert on public.jawnix_lead_requests;
create trigger jawnix_snapshot_states_before_insert
  before insert on public.jawnix_lead_requests
  for each row execute function public.jawnix_snapshot_states();

