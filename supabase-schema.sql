do $$
begin
  if not exists (
    select 1
    from pg_constraint
    where conname = 'jawnix_lead_requests_fulfillable_count_check'
      and conrelid = 'public.jawnix_lead_requests'::regclass
  ) then
    alter table public.jawnix_lead_requests
      add constraint jawnix_lead_requests_fulfillable_count_check
      check (lead_count between 1 and 2147483647)
      not valid;
  end if;
end $$;

