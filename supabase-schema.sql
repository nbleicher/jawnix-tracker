create table if not exists public.jawnix_settings (
  workspace_id text primary key,
  default_rate numeric(12, 6) not null default 0.0015,
  invoice_counter integer not null default 1 check (invoice_counter > 0),
  updated_at timestamptz not null default now()
);

create table if not exists public.jawnix_leads (
  workspace_id text not null,
  id bigint generated by default as identity,
  date date not null,
  client text not null check (length(trim(client)) > 0),
  email text not null default '',
  leads integer not null check (leads > 0),
  rate numeric(12, 6) not null check (rate > 0),
  amount numeric(12, 2) generated always as (round(leads::numeric * rate, 2)) stored,
  created_at timestamptz not null default now(),
  primary key (workspace_id, id)
);

create table if not exists public.jawnix_invoice_voids (
  workspace_id text not null,
  week_start date not null,
  client text not null check (length(trim(client)) > 0),
  voided_at timestamptz not null default now(),
  primary key (workspace_id, week_start, client)
);

create table if not exists public.jawnix_invoice_records (
  workspace_id text not null,
  week_start date not null,
  client text not null check (length(trim(client)) > 0),
  invoice_num text not null check (length(trim(invoice_num)) > 0),
  checkout_session_id text not null default '',
  checkout_url text not null default '',
  pdf_filename text not null default '',
  total_due numeric(12, 2) not null default 0,
  payment_status text not null default 'unpaid' check (payment_status in ('paid', 'unpaid', 'expired', 'no_payment_required')),
  paid_at timestamptz,
  generated_at timestamptz not null default now(),
  voided_at timestamptz,
  primary key (workspace_id, week_start, client, invoice_num)
);

alter table public.jawnix_invoice_records
  add column if not exists payment_status text not null default 'unpaid'
    check (payment_status in ('paid', 'unpaid', 'expired', 'no_payment_required')),
  add column if not exists paid_at timestamptz;

do $$
declare
  constraint_name text;
begin
  for constraint_name in
    select con.conname
    from pg_constraint con
    join pg_class rel on rel.oid = con.conrelid
    join pg_namespace nsp on nsp.oid = rel.relnamespace
    where nsp.nspname = 'public'
      and rel.relname = 'jawnix_invoice_records'
      and con.contype = 'c'
      and pg_get_constraintdef(con.oid) like '%payment_status%'
  loop
    execute format('alter table public.jawnix_invoice_records drop constraint if exists %I', constraint_name);
  end loop;
end $$;

