create table if not exists public.jawnix_misc_income (
  workspace_id text not null,
  id bigint generated by default as identity,
  income_date date not null,
  notes text not null default '',
  amount numeric(12, 2) not null check (amount >= 0),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  primary key (workspace_id, id)
);

do $$
begin
  if to_regclass('public.jawnix_weekly_financials') is not null then
    insert into public.jawnix_expenses (
      workspace_id,
      category,
      vendor,
      expense_date,
      billing_cycle,
      notes,
      amount,
      created_at,
      updated_at
    )
    select
      wf.workspace_id,
      'Lead costs',
      '',
      wf.week_start,
      'Migrated weekly lead costs',
      wf.notes,
      wf.lead_costs,
      coalesce(wf.updated_at, now()),
      coalesce(wf.updated_at, now())
    from public.jawnix_weekly_financials wf
    where wf.lead_costs > 0
      and not exists (
        select 1
        from public.jawnix_expenses e
        where e.workspace_id = wf.workspace_id
          and e.expense_date = wf.week_start
          and e.category = 'Lead costs'
          and e.billing_cycle = 'Migrated weekly lead costs'
          and e.amount = wf.lead_costs
          and e.notes = wf.notes
      );
  end if;
end $$;

